package zental.gr2_lhj;

public class Faq {
	private String title;
	private String detail;
	private String img;
	
	public Faq(String title, String detail, String img) {
		super();
		this.title = title;
		this.detail = detail;
		this.img = img;
	}

	public String getTitle() {
		return title;
	}

	public String getDetail() {
		return detail;
	}

	public String getImg() {
		return img;
	}
	
	
}
